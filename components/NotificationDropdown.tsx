'use client';

import React, { useState, useEffect, useRef } from 'react';

interface Notification {
  id: string;
  userId: string;
  type: 'achievement' | 'level_up' | 'system' | 'community' | 'reward';
  title: string;
  message: string;
  isRead: boolean;
  createdAt: string;
  metadata?: Record<string, any>;
}

interface NotificationDropdownProps {
  userId?: string;
}

